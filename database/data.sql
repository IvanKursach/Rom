-- Показать всех тренеров с их тренировками
SELECT 
    t.id AS 'ID тренера',
    t.name AS 'Имя тренера',
    COUNT(tr.id) AS 'Кол-во тренировок'
FROM trainers t
LEFT JOIN trainings tr ON t.id = tr.trainer_id
GROUP BY t.id, t.name
ORDER BY t.id;

-- Показать все тренировки по дням недели
SELECT 
    day AS 'День недели',
    COUNT(*) AS 'Кол-во тренировок',
    GROUP_CONCAT(name SEPARATOR ', ') AS 'Список тренировок'
FROM trainings
GROUP BY day
ORDER BY FIELD(day, 'monday', 'tuesday', 'wednesday', 'thursday', 'friday', 'saturday', 'sunday');